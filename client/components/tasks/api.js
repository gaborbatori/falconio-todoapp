define([], function(){
//-------------------------------------------------------------------------------
	var path = "/tasks";

	return {
		get: get,
		add: add,
		update: update,
		del: del
	};
	//-------------------------------------------------------------------------------
	function get(){
	//-------------------------------------------------------------------------------
		return $.ajax(window.application.provider.host + path, { type: "GET", dataType: "json" });
	}
	//-------------------------------------------------------------------------------
	function add(data){
	//-------------------------------------------------------------------------------
		return $.ajax(window.application.provider.host + path, { type: "POST", dataType: "json", data: data });
	}
	//-------------------------------------------------------------------------------
	function update(id, data){
	//-------------------------------------------------------------------------------
		return $.ajax(window.application.provider.host + path + "/" + id, { type: "PATCH", dataType: "json", data: data });
	}
	//-------------------------------------------------------------------------------
	function del(id){
	//-------------------------------------------------------------------------------
		return $.ajax(window.application.provider.host + path + "/" + id, { type: "DELETE", dataType: "json" });
	}
	//-------------------------------------------------------------------------------
});
//-------------------------------------------------------------------------------

function safehtml(html){
//-------------------------------------------------------------------------------
	return $("<div />", { html: html }).html();
}
//-------------------------------------------------------------------------------
function escapehtml(html){
//-------------------------------------------------------------------------------
	return $("<div />", { text: html }).html();
}
//-------------------------------------------------------------------------------
function nl2br(html){
//-------------------------------------------------------------------------------
	return (html || "").replace(/([^>\r\n]?)(\r\n|\n\r|\r|\n)/g, "$1<br />$2");
}

define(["modal", "text!./error message.html"], function(modal, tpl){
//-------------------------------------------------------------------------------
	var template = $.templates(tpl);

	$(document).ajaxError(onGlobalError);
	//-------------------------------------------------------------------------------
	function onGlobalError(event, xhr, ajaxSettings){
	//-------------------------------------------------------------------------------
		modal.open(template.render(xhr));
	}
	//-------------------------------------------------------------------------------
});
